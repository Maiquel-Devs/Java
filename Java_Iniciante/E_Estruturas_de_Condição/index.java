package Java_Iniciante.E_Estruturas_de_Condição;

public class index {
    
    public static void main(String[] args) {
        int a = 10;

        if (a > 10) {
            System.out.println(a + " é maior que 10");
        }
        else if (a < 10) {
            System.out.println(a + " é menor que 10");
        }
        else {
            System.out.println(a + " é igual a 10");
        }

    }
}
/* 
Operadores de Comparação

     ==  → igual a
     !=  → diferente de
     >   → maior que
     <   → menor que
     >=  → maior ou igual a
     <=  → menor ou igual a
*/

