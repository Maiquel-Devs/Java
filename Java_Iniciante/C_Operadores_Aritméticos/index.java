package Java_Iniciante.C_Operadores_Aritméticos;

public class index {

    public static void main(String[] args) {
        int a = 10;
        int b = 2;

        System.out.println("a + b = " + (a + b)); // 12 (adição)
        System.out.println("a - b = " + (a - b)); // 8 (subtração)
        System.out.println("a * b = " + (a * b)); // 20 (multuplicação)
        System.out.println("a / b = " + (a / b)); // 5 (divisão)
        System.out.println("a % b = " + (a % b)); // 0 (resto da divisão)
    }
    
}
